// 전문가 워크플로가 설계한 상세 블루프린트(매체 5종)를 기본값 위에 병합한다.
let ENRICHED = {};
try { ENRICHED = require("./design-blueprints.json"); } catch { ENRICHED = {}; }
const DESIGN_BLUEPRINTS = { ...BASE, ...ENRICHED };

function designElements(medium) {
  return DESIGN_BLUEPRINTS[resolveMedium(medium)] || [];
}

/** 매체×포맷의 추천 설계값 객체 { key: 추천방향 }. UI 자동 채움·프롬프트 주입용. */
function recommendedDesignSpec(medium, format) {
  const f = resolveFormat(format);
  const out = {};
  designElements(medium).forEach((e) => { out[e.key] = e.recommend[f] || e.recommend.medium || ""; });
  return out;
}

/** 작가가 확정한 설계값(input.designSpec)을 프롬프트 구속 블록으로. (없으면 "") */
function buildDesignSpecBlock(input) {
  const medium = resolveMedium(input && input.medium);
  const spec = input && input.designSpec;
  if (!spec || typeof spec !== "object") return "";
  const els = designElements(medium);
  const lines = els.map((e) => {
    const v = String(spec[e.key] ?? "").trim();
    return v ? `- ${e.label}: ${v}` : null;
  }).filter(Boolean);
  if (!lines.length) return "";
  return `# 상세 설계 요소 (작가가 노브 단위로 확정한 구속 조건 — 모든 산출물이 이 값을 정확히 따른다)\n${lines.join("\n")}`;
}

module.exports = {
  DESIGN_BLUEPRINTS, designElements, recommendedDesignSpec, buildDesignSpecBlock,
};
