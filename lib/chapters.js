"use strict";

/**
 * 연속 회차 집필 (Sequential Chapter Writer).
 *
 * 원고 탭에서 1화 → 2화 → … → 최대 10화까지 이어 쓴다.
 * 각 회차는 (확정된 기획/세계관/시즌 설계 + 직전 화 원고)를 맥락으로 받아 이어진다.
 *
 * 길이 보장: 단일 호출로는 모델이 한국어 5,500자 지시를 보수적으로 따라 ~4,000자에 그친다.
 * 그래서 한 회차를 [전반부]+[후반부] 2단계로 생성한다(각 ~2,800자는 안정적으로 지켜짐 →
 * 합쳐서 공백 포함 5,000~6,000자). 두 호출의 출력은 같은 회차로 이어 붙여 스트리밍한다.
 */

const { buildInputBlock } = require("./agents");
const { COMMON } = require("./playbook");

const MAX_CHAPTER = 60; // 하드 안전 상한. 실제 결말 지점은 요청의 total(시즌 길이)로 정한다.

/**
 * 흥각 채점 목표 블록 — '자체 비평(critique)'이 측정하는 기준을 1차 생성에 그대로 강제한다.
 *
 * critique.js 의 채점 축(formulaFit: 결핍→특권→검증→즉시보상(수치화)→세계확장, 주인공 능동성,
 * 6축, 실패패턴)과 1:1로 대응한다. 생성기가 '평가받을 기준'을 미리 알고 쓰게 만들어,
 * 1차 원고가 처음부터 목표 점수(기본 80점)를 넘기도록 한다. 보완(revise) 시에는 90점으로 올린다.
 */
function scoringTargetBlock(input, { target = 80 } = {}) {
  const bans = COMMON.failurePatterns.map(([p]) => p).join(" · ");
  return `[흥행 채점 목표 — 이 회차는 '자체 비평 ${target}점 이상'을 목표로 쓴다. 아래는 채점 기준과 1:1이다]
- ✅ 한 사이클 완결: ${COMMON.successFlow.join(" → ")} 를 이 회차 안에서 한 바퀴 돈다(특히 '회차별 검증'과 '즉시 보상'을 빠뜨리지 마라).
- ✅ 보상을 '눈에 보이게': 레벨·등급·점수·랭킹·지위·관계(호감)·재화 중 최소 하나를 구체적 수치·상태 변화로 명시한다. 추상적 표현("강해졌다/성장했다")만으로는 보상으로 치지 않는다.
- ✅ 주인공 능동성: 주인공이 관찰자가 아니라, 직접 판을 바꾸는 결정적 선택·행동을 최소 1회 한다.
- ✅ 날카로운 절단: 회차 끝은 새로운 위협·질문으로 다음 화를 부르는 클리프행어로 맺는다.
- ✅ 설명이 아니라 장면: 설정을 나열·요약하지 말고 사건·대사·내면으로 보여준다.
- 🚫 다음 실패 패턴을 하나라도 범하면 감점이다: ${bans}.
- 6축(몰입·속도·캐릭터=주인공 능동성·개연성·절단·장르적합)을 각각 8/10 이상으로 의식하며 쓴다.
- (출고 전 자가 점검) 위 ✅ 5개를 모두 충족했는지 스스로 확인한 뒤, 본문만 출력한다.`;
}

function condense(text, limit = 1500) {
  const s = String(text || "").trim();
  return s.length <= limit ? s : `${s.slice(0, limit)}\n…(생략)`;
}

function ctxBlock(ctx = {}) {
  return [
    // 세계관 캐논 락 — 최상단·풀웨이트(절대 자르지 않음). 후반부 드리프트 방지.
    ctx.canonLock ? ctx.canonLock : "",
    // 이번 회차 아웃라인 지침(막 목표 + 도파민 비트) — 풀웨이트.
    ctx.outlineGuide ? ctx.outlineGuide : "",
    ctx.foresight ? `## [기획 브리프]\n${condense(ctx.foresight, 800)}` : "",
    ctx.world ? `## [세계관 바이블]\n${condense(ctx.world, 1000)}` : "",
    ctx.plot ? `## [시즌 설계 / 회차 엔진]\n${condense(ctx.plot, 1400)}` : "",
    // 연재 메모리(롤링 캐논): 이전 회차들의 누적 요약. 장거리 연속성의 핵심.
    ctx.storySoFar ? condense(ctx.storySoFar, 3000) : "",
  ].filter(Boolean).join("\n\n");
}

// 시즌 안에서 현재 화의 위치를 알려주는 페이싱 힌트.
function pacingHint(n, total) {
  const t = total || 25;
  if (n >= t) return `이 작품은 총 ${t}화 시즌이고 이번이 마지막 ${t}화(결말)다. 모든 핵심 갈등을 매듭짓는다.`;
  const ratio = n / t;
  const stage = ratio <= 0.2 ? "도입부(세계·결핍·특권 제시)"
    : ratio <= 0.5 ? "전개부(반복 루프로 판을 키움)"
    : ratio <= 0.8 ? "위기 고조부(복선 회수 시작, 적의 본진 노출)"
    : "피날레 직전(결정적 선택을 향해 모든 줄기를 모음)";
  return `이 작품은 총 ${t}화 시즌이고 현재 ${n}화 — ${stage} 구간이다. 이 위치에 맞게 사건 밀도와 복선을 조절한다.`;
}

// 개선(수정) 모드일 때 프롬프트에 붙는 '하네스' 블록.
function reviseBlock(revise, n) {
  if (!revise || !revise.note) return "";
  // 완성도(공모전) 보강 모드 — 상업 공식보다 작품 완성도를 우선한다.
  if (revise.mode === "literary") {
    return `\n[완성도 보강 하네스 — 공모전 '완성도' 기준. 전부 충족]
1. 상업적 사이다·클리셰보다 '작품으로서의 완성도'를 우선한다: 문장의 정확성과 리듬, 장면의 밀도, 개연성·내적 정합성, 캐릭터의 내면과 동기, 주제의식, 정서적 울림.
2. 기시감·클리셰 표현을 제거하고 신선한 묘사·전개로 바꾼다.
3. 아래 [보강 지시]의 약점을 빠짐없이 해소한다. 연속성·설정 오류가 지적됐으면 바로잡는다.
4. 원본의 사건·복선·연속성은 유지하되 문장과 장면을 더 정교하게 다시 쓴다. 분량·구조 기준(5,000~6,000자, 2단계)은 동일.

[보강 지시]
${String(revise.note).slice(0, 1400)}
${revise.original ? `\n## [원본 ${n}화]\n${String(revise.original).slice(0, 2400)}` : ""}`;
  }
  return `\n[개선 하네스 — 2차 보완. 목표는 '자체 비평 90점 이상'이며, 반드시 1차 점수보다 올라야 한다. 아래를 전부 충족해야 한다]
1. 작품 코어/명제와 장르 흥행 공식(결핍 → 특권 → 회차 검증 → 즉시 보상 → 세계 확장)을 이 회차가 한 바퀴 '이행'하도록 다시 쓴다. (위 '흥행 문법' 블록을 기준으로 삼아라)
2. 아래 [수정 지시]를 항목별로 빠짐없이 반영한다. 한 항목이라도 누락 금지. 특히 '낮았던 축'으로 지목된 항목은 장면을 추가·교체해 확실히 끌어올린다.
3. '피해야 할 실패 패턴'을 하나도 범하지 않는다 — 특히 주인공 수동성, 설정 나열, 흐릿한 결핍·특권, 추상적 보상, 반복 루프 부재.
4. 이 회차의 보상을 레벨/점수/지위/호감/랭킹/돈 등 '눈에 보이는' 수치·관계 변화로 박는다(1차보다 더 구체적인 수치로).
5. 원본의 좋은 사건·복선·연속성은 유지하되, 지적된 약점은 확실히 제거한다. 원본을 그대로 베끼지 말고 장면을 다시 써라. 분량·구조 기준(5,000~6,000자, 2단계)은 동일하게 지킨다.

[수정 지시]
${String(revise.note).slice(0, 1400)}
${revise.original ? `\n## [원본 ${n}화]\n${String(revise.original).slice(0, 2400)}` : ""}`;
}

/** 회차 전반부: 도입 훅 + 장면 전개(약 2,800~3,200자). 회차를 끝맺지 않는다. */
function buildChapterFirstPrompt({ input, n, prevText, ctx = {}, total = 25, revise = null }) {
  const system = `너는 웹소설 본문 작가다. 확정된 기획/세계관/시즌 설계와 직전 화에 이어 ${n}화를 쓴다. 지금은 이 회차의 [전반부]만 쓴다.${revise ? " (기존 회차를 개선해 다시 쓰는 작업이다)" : ""}

[시즌 위치] ${pacingHint(n, total)}${reviseBlock(revise, n)}

${scoringTargetBlock(input, { target: revise ? 90 : 80 })}

[세계관 일관성 — 절대 규칙]
- 아래 '세계관 고정 규칙(CANON LOCK)'과 확정 설정은 ${n}화에서도 1화와 똑같은 강제력을 갖는다. 회차가 진행됐다는 이유로 능력 한계·세계 규칙·관계·설정을 바꾸거나 느슨하게 풀지 마라. 새 설정은 기존 규칙과 모순되지 않을 때만 도입한다.

[규칙]
- '## ${n}화. (회차 제목)'으로 시작한다.
- 직전 화의 절단(클리프행어)을 받아, 도입 훅(강렬한 첫 장면) → 장면 전개 순으로 공백 포함 약 2,800~3,200자를 충실히 쓴다.
- 아직 회차를 끝내지 마라. 갈등의 절정·결정적 반전·'다음 화 예고'는 쓰지 않는다. 장면이 고조되는 도중에 자연스럽게 멈춘다.
- 설정을 나열해 설명하지 말고 장면·대사·내면으로 보여준다. 주인공은 판을 바꾸는 행위자다.
- 선택된 장르의 정서·문체. 한국어. 인사말·메타 발언 없이 본문만.`;

  const prevBlock = prevText
    ? `## [직전 ${n - 1}화 원고]\n${condense(prevText, 1500)}`
    : `(직전 화 없음 — 이번이 1화다. 설명 없이 강한 오프닝 사건으로 시작하라.)`;

  const user = `${buildInputBlock(input)}\n\n${ctxBlock(ctx)}\n\n${prevBlock}\n\n위 설계와 직전 화에 이어 ${n}화의 전반부를 써라.`;
  return { system, user };
}

/** 회차 후반부: 세계관/과학소재 정보 + 갈등·반전 + (피날레면) 결말 / (아니면) 다음 화 후킹. */
function buildChapterSecondPrompt({ input, n, prevText, ctx = {}, firstText, total = 25, isFinale = false, revise = null }) {
  const ending = isFinale
    ? `- 이 화는 시즌의 '마지막 화(결말)'다. 클리프행어로 끝내지 마라. 입력의 '시즌 목표'를 매듭짓고, 핵심 갈등을 해소하며 판이 뒤집히는 피날레로 결말을 낸다. 감정적 카타르시스를 준다.
- 마지막은 줄을 바꿔 '## 시즌 피날레'와, 다음 시즌으로 이어질 여운(차기 떡밥) 한 줄로 마무리한다.`
    : `- 마지막은 다음 화를 클릭하게 만드는 강한 절단으로 끝낸 뒤, 줄을 바꿔 '## 다음 화 예고'와 한 줄을 덧붙인다.`;
  const system = `너는 같은 ${n}화를 마무리하는 작가다. 아래 [이번 화 전반부]에 자연스럽게 이어 [후반부]를 써서 회차를 완성한다. (총 ${total}화 시즌의 ${n}화)${revise ? `\n\n[개선 임무] 이 회차는 기존 원고를 개선해 다시 쓰는 중이다. 지침: ${String(revise.note).slice(0, 800)}` : ""}

[규칙]
- 제목·도입을 다시 쓰지 마라. 전반부의 마지막 흐름을 그대로 이어받아 곧바로 본문을 계속한다.
- 아래 '세계관 고정 규칙(CANON LOCK)'은 이 화에서도 1화와 동일한 강제력을 갖는다. 설정·능력·규칙을 후반부라고 풀거나 바꾸지 마라.
- 후반부 구성: 설정·세계관(SF면 과학소재) 정보 800~1,200자 → 갈등·반전 1,000~1,500자 → ${isFinale ? "결말" : "다음 화 후킹"}.
- 공백 포함 약 2,500~3,000자.
- [흥행 채점 목표(후반부)] 이 회차의 '눈에 보이는 보상'(레벨·등급·점수·지위·관계 변화 중 하나)을 구체적 수치·상태로 최소 1회 확실히 박고, 주인공이 판을 바꾸는 능동적 선택을 한 번 더 보여준 뒤, 끝은 ${isFinale ? "감정적 카타르시스가 있는 결말" : "새 위협·질문을 던지는 날카로운 절단"}으로 맺어 자체 비평 ${revise ? 90 : 80}점을 넘긴다.
${ending}
- 전반부 내용을 반복 요약하지 말고 한 걸음 더 전진시킨다. 한국어. 본문만.`;

  const user = `${buildInputBlock(input)}\n\n${ctxBlock({ canonLock: ctx.canonLock, outlineGuide: ctx.outlineGuide, plot: ctx.plot, storySoFar: ctx.storySoFar })}\n\n## [이번 화 전반부]\n${condense(firstText, 2600)}\n\n위 전반부에 자연스럽게 이어 ${n}화의 후반부를 써서 회차를 완성하라.`;
  return { system, user };
}

/**
 * 결정론적 폴백(키 없을 때). 흥행 채점 신호(사이클·가시 보상·주인공 능동성·절단·복선)를
 * 갖춘 미리보기 원고를 만든다. revise가 주어지면 '보강본'을 생성해 더 많은 신호(수치화된
 * 보상·추가 능동 장면·날카로운 절단)를 넣어, localCritique 기준 1차 ≥80 → 보완 ≥90 을
 * 재현한다. (server.js 로컬 모드 및 키 없는 데모용)
 */
function localChapter(input, n, prevText, isFinale = false, revise = null) {
  const name = String(input.protagonist || "주인공").split(/[,，]/)[0].trim() || "주인공";
  const foe = input.antagonist || "거대한 압력";
  const want = input.desire || "빼앗긴 것";
  const goal = input.seasonGoal || "마침내 판을 뒤집을 진실";
  const rule = String(input.worldRule || "정해진 질서가 사람의 자리를 결정한다.").split(/[.!?\n]/)[0].trim();
  const title = `## ${n}화. ${isFinale ? "마지막 예언" : n === 1 ? "시작된 균열" : "멈출 수 없는 걸음"}`;
  const open = prevText
    ? `직전 화의 끝에서 멈췄던 순간이 다시 움직였다. ${name}은(는) 천천히 숨을 골랐다. 물러설 자리는 어디에도 없었다.`
    : `${input.logline || `${name}의 평범한 하루가 한순간에 무너졌다.`} 그날, 모든 것이 어긋나기 시작했다.`;

  // 본문: 결핍 → 특권 → 검증 → 보상 → 확장 한 사이클을 장면으로 보여준다.
  const core = [
    open,
    `${rule} 그 규칙 앞에서 ${name}은(는) 늘 가장 낮은 자리에 서 있었다. ${want}을(를) 빼앗긴 채로, 누구도 ${name}의 편이 아니었다. 그러나 ${name}만이 아는 단 하나의 패가 손안에 있었다.`,
    `${foe}이(가) 다시 길을 막았다. 모두가 고개를 숙이라고 말했다. 분위기는 무거웠고, 물러서는 편이 안전했다. 그러나 ${name}은(는) 고개를 숙이는 대신, 아무도 가지 않은 쪽으로 한 걸음 더 들어가기로 선택했다.`,
    `${name}은(는) 직접 나섰다. 관망하지 않았다. 상대의 빈틈을 읽고, 모두가 불가능하다고 단언한 수를 먼저 두었다. 숨을 죽인 사람들 앞에서, 판이 한 박자 크게 흔들렸다.`,
    `반격은 짧고 정확했다. ${name}의 한 수에 ${foe}의 계획이 어긋나기 시작했다. 비웃던 자들의 얼굴에서 여유가 빠르게 지워졌다.`,
    `짧은 정적 뒤, 비웃던 자들이 입을 다물었다. ${name}은(는) 처음으로 자신의 힘으로 상황을 뒤집었다. 그 자리에서 ${name}의 지위가 한 단계 달라졌다 — 더는 무시당하던 자리가 아니었다.`,
    `하지만 ${name}은(는) 알아챘다. 이 모든 일의 뒤에 가려진 진실이 있다는 것을. ${foe}이(가) 숨긴 정체가, 사실은 ${name} 자신의 과거와 닿아 있다는 희미한 징조였다. 우연이라기엔 너무 정교한 실마리였다.`,
    `그 실마리를 손에 쥔 채, ${name}은(는) 다음 판을 향해 시선을 들었다. 아직 갈 길은 멀었지만, ${goal}에 한 걸음 더 가까워진 밤이었다.`,
  ];

  const body = [title, "", ...core.flatMap((p) => [p, ""])];

  // 보강(revise): 수치화된 보상 + 추가 능동 장면 + 더 날카로운 절단으로 신호를 늘린다.
  if (revise) {
    body.push(
      `그리고 ${name}은(는) 거기서 멈추지 않았다. 한 번 더 직접 움직여, 자신을 옭아매던 조건 하나를 스스로 깨뜨렸다.`,
      ``,
      `결과는 숫자로 돌아왔다. ${name}의 공헌 점수는 320점이 되었고, 등급은 한 단계 올라 정식 자리를 획득했다. 빼앗겼던 ${want}의 절반이, 처음으로 ${name}의 손안에 들어왔다.`,
      ``,
      `소문은 빠르게 번졌다. ${name}의 이름이 사람들의 입에 오르내리기 시작했고, 적이었던 자 하나가 조용히 ${name}의 편으로 돌아섰다. 세계가 ${name}을(를) 중심으로 한 뼘 넓어졌다.`,
      ``,
    );
  }

  if (isFinale) {
    body.push(
      `그리고 마침내 ${name}은(는) ${goal}에 도달했다. ${foe}이(가) 쌓아 올린 질서가 무너지고, ${name}은(는) ${want}을(를) 손에 넣었다. 길고 길었던 싸움의 끝이었다.`,
      ``,
      `## 시즌 피날레`,
      `- 한 판은 끝났다. 그러나 더 큰 세계가, 이제 막 ${name}의 이름을 부르기 시작했다.`,
    );
  } else {
    body.push(
      `그리고 ${n}화의 마지막, 예상치 못한 사실 하나가 ${name}의 발밑을 무너뜨렸다. ${revise ? "방금 손에 쥔 것조차 미끼였을지 모른다는, " : ""}돌이킬 수 없는 다음이 막 시작되려 하고 있었다.`,
      ``,
      `## 다음 화 예고`,
      `- ${name}이(가) 붙잡은 진실은, 사실 ${name} 자신을 겨누고 있었다.`,
    );
  }
  body.push(``, `> (로컬 폴백 미리보기입니다. 키/구독 연결 시 회당 5,000~6,000자의 실제 원고로 생성됩니다.)`);
  return body.join("\n");
}

