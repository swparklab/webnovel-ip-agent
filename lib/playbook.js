function getPlaybook(genreKey) {
  const base = GENRES[genreKey] || GENRES.aiForesight;
  const key = GENRES[genreKey] ? genreKey : "aiForesight";
  return {
    ...base,
    key,
    family: SF_FAMILY.has(key) ? "sf" : "general",
    preset: PRESETS[key] || null,
  };
}

function rows(pairs) {
  return pairs.map(([a, b]) => `| ${a} | ${b} |`).join("\n");
}

/** Genre formula injected into every agent so generation follows proven success structure. */
